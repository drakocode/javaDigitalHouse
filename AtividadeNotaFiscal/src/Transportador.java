public class Transportador {
    private  String nome;
    private int cnpj;
    private int ie;
    private int antt;



    //getters and setters
    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getCnpj() {
        return cnpj;
    }

    public void setCnpj(int cnpj) {
        this.cnpj = cnpj;
    }

    public int getIe() {
        return ie;
    }

    public void setIe(int ie) {
        this.ie = ie;
    }

    public int getAntt() {
        return antt;
    }

    public void setAntt(int antt) {
        this.antt = antt;
    }
}
